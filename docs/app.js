const state = {
  language: "en",
  selectedFilter: "all"
};

function translatePage() {
  const keys = document.querySelectorAll("[data-i18n]");
  keys.forEach((element) => {
    const phrase = element.dataset.i18n;
    const translated = translations[state.language]?.[phrase];
    if (translated) {
      element.textContent = translated;
    }
  });
}

function setLanguage(lang) {
  state.language = lang;
  document.querySelectorAll(".lang-switcher button").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });
  translatePage();
}

function renderGallery(items) {
  const galleryGrid = document.querySelector(".gallery-grid");
  galleryGrid.innerHTML = items
    .map(
      (item) => `
        <article class="gallery-item" data-id="${item.id}">
          <img src="${item.image}" alt="${item.title}" loading="lazy" />
          <div class="gallery-copy">
            <h3>${item.title}</h3>
            <p>${item.note}</p>
            <span class="tag">${item.category}</span>
          </div>
        </article>
      `
    )
    .join("");
}

function renderTestimonials() {
  const testimonialsWrapper = document.querySelector(".testimonial-cards");
  testimonialsWrapper.innerHTML = testimonials
    .map(
      (item) => `
        <article class="testimonial-card">
          <p>“${item.quote}”</p>
          <div class="testimonial-meta">
            <strong>${item.customerName}</strong>
            <span class="rating">${"★".repeat(item.rating)}</span>
          </div>
        </article>
      `
    )
    .join("");
}

function renderHappyCustomers() {
  const gallery = document.querySelector(".happy-gallery-grid");
  if (!gallery) return;
  gallery.innerHTML = happyCustomers
    .map(
      (item) => `
        <article class="happy-gallery-item" data-id="${item.id}" tabindex="0" role="button" aria-label="View ${item.name} photo">
          <img src="${item.image}" alt="${item.name}" loading="lazy" />
          <span>${item.name}</span>
        </article>
      `
    )
    .join("");
}

function filterGallery(filter) {
  state.selectedFilter = filter;
  document.querySelectorAll(".gallery-filters button").forEach((button) => {
    button.classList.toggle("active", button.dataset.filter === filter);
  });
  const results = filter === "all" ? galleryItems : galleryItems.filter((item) => item.category === filter);
  renderGallery(results);
}

function openGalleryModal(item) {
  const modal = document.getElementById("gallery-modal");
  const modalImg = document.getElementById("modal-img");
  modalImg.src = item.image;
  modalImg.alt = item.title || "Happy customer photo";
  document.getElementById("modal-title").textContent = item.title;
  document.getElementById("modal-category").textContent = item.category.toUpperCase();
  document.getElementById("modal-note").textContent = item.note;
  modal.setAttribute("aria-hidden", "false");
}

function closeGalleryModal() {
  const modal = document.getElementById("gallery-modal");
  modal.setAttribute("aria-hidden", "true");
}

function attachEvents() {
  const navToggle = document.querySelector(".nav-toggle");
  navToggle.addEventListener("click", () => {
    const expanded = navToggle.getAttribute("aria-expanded") === "true";
    navToggle.setAttribute("aria-expanded", String(!expanded));
    document.body.classList.toggle("nav-open");
  });

  document.querySelectorAll(".lang-switcher button").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
  });

  // SPA nav routing: intercept nav link clicks and navigate via hash router
  document.querySelectorAll('.nav-links a').forEach((link) => {
    link.addEventListener('click', (e) => {
      const href = link.getAttribute('href');
      if (href && href.startsWith('#')) {
        e.preventDefault();
        const route = href.replace(/^#/, '') || 'home';
        navigateTo(route);
      }
    });
  });

  document.querySelectorAll(".gallery-filters button").forEach((button) => {
    button.addEventListener("click", () => filterGallery(button.dataset.filter));
  });

  const galleryGrid = document.querySelector(".gallery-grid");
  galleryGrid.addEventListener("click", (event) => {
    const card = event.target.closest(".gallery-item");
    if (!card) return;
    const itemId = Number(card.dataset.id);
    const item = galleryItems.find((entry) => entry.id === itemId);
    if (item) openGalleryModal(item);
  });

  const happyGrid = document.querySelector(".happy-gallery-grid");
  if (happyGrid) {
    happyGrid.addEventListener("click", (event) => {
      const card = event.target.closest(".happy-gallery-item");
      if (!card) return;
      const itemId = Number(card.dataset.id);
      const item = happyCustomers.find((entry) => entry.id === itemId);
      if (item) {
        openGalleryModal({
          image: item.image,
          title: item.name,
          category: "Happy Customer",
          note: item.note
        });
      }
    });

    happyGrid.addEventListener("keydown", (event) => {
      if (event.key !== "Enter" && event.key !== " ") return;
      const card = event.target.closest(".happy-gallery-item");
      if (!card) return;
      event.preventDefault();
      const itemId = Number(card.dataset.id);
      const item = happyCustomers.find((entry) => entry.id === itemId);
      if (item) {
        openGalleryModal({
          image: item.image,
          title: item.name,
          category: "Happy Customer",
          note: item.note
        });
      }
    });
  }

  const modal = document.getElementById("gallery-modal");
  modal.addEventListener("click", (event) => {
    if (event.target === modal || event.target.closest(".modal-close")) {
      closeGalleryModal();
    }
  });

  const form = document.querySelector(".inquiry-form");
  const status = form.querySelector(".form-status");
  form.addEventListener("submit", (event) => {
    event.preventDefault();
    const name = document.getElementById("inq-name").value.trim();
    const phone = document.getElementById("inq-phone").value.trim();
    if (!name || !phone) {
      status.textContent = "Please enter your name and phone number.";
      return;
    }
    status.textContent = "Thank you! Your inquiry has been sent. We will contact you shortly.";
    form.reset();
  });
}

// --- Simple hash-based router ---
function updateActiveNav(route) {
  document.querySelectorAll('.nav-links a').forEach((a) => {
    const target = a.getAttribute('href')?.replace(/^#/, '') || 'home';
    if (target === route) {
      a.setAttribute('aria-current', 'page');
    } else {
      a.removeAttribute('aria-current');
    }
  });
}

function navigateTo(route) {
  if (!route) route = 'home';
  // update the hash which triggers the router
  location.hash = `#${route}`;
}

function handleHashChange() {
  const route = location.hash.replace(/^#/, '') || 'home';
  const section = document.getElementById(route) || document.getElementById('home');
  if (section) {
    section.scrollIntoView({ behavior: 'smooth', block: 'start' });
    updateActiveNav(route);
  }
}

function routerInit() {
  // handle initial load
  if (!location.hash) {
    location.hash = '#home';
  }
  handleHashChange();
  window.addEventListener('hashchange', handleHashChange);
}

function init() {
  setLanguage(state.language);
  renderGallery(galleryItems);
  renderTestimonials();
  renderHappyCustomers();
  attachEvents();
  routerInit();
}

document.addEventListener("DOMContentLoaded", init);
